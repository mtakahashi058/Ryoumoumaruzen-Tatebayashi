INSERT INTO D_取引
(
	--取引ID,
	取引日,
	拠点ID,
	伝票No,
	計量ID,
	取引区分,
	車両ID,
	得意先ID,
	得意先名称,
	項目1ID,
	項目1名称,
	項目2ID,
	項目2名称,
	備考,
	伝票消費税額,
	行No,
	品目ID,
	品目名称,
	数量,
	単位ID,
	単価,
	金額,
	消費税額,
	明細備考,
	完了区分,
	更新日時,
	作成日時
)
SELECT
	X.計量日,
	X.拠点ID,
	X.伝票No,
	X.計量ID,
	ISNULL(H.取引区分, 1) AS 取引区分,	-- 1: 売上 2: 仕入
	X.車両ID,
	X.得意先ID,
	X.得意先名称,
	X.項目1ID,
	X.項目1名称,
	X.項目2ID,
	X.項目2名称,
	X.備考,
	X.伝票消費税額,
	X.行No,
	X.品目ID,
	X.品目名称,
	X.数量,
	X.単位ID,
	X.単価,
	X.金額,
	X.消費税額,
	X.明細備考,
	X.完了区分,
	X.更新日時,
	X.作成日時
FROM
	D_計量 AS X
	LEFT OUTER JOIN M_品目 AS H ON X.品目ID = H.品目ID
